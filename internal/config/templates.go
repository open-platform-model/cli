// Package config provides configuration loading and management.
package config

import "fmt"

// DefaultRegistry is the default CUE_REGISTRY value used when initializing a
// fresh OPM configuration (the `config.registry` value in the generated
// config.cue).
const DefaultRegistry = "testing.opmodel.dev=ghcr.io/open-platform-model,opmodel.dev=ghcr.io/open-platform-model,registry.cue.works"

// DefaultConfigTemplate is the embedded template for ~/.opm/config.cue.
// Scalar data only — no CUE imports, no providers (enhancement 0006 D39).
var DefaultConfigTemplate = fmt.Sprintf(`// OPM CLI Configuration
// Generated by: opm config init
// Documentation: https://opmodel.dev/docs/cli/config
package config

config: {
	// registry is the default registry for CUE module resolution.
	// Override with --registry flag or OPM_REGISTRY env var.
	registry: %q

	kubernetes: {
		// kubeconfig is the path to the kubeconfig file.
		// Override with --kubeconfig flag or OPM_KUBECONFIG env var.
		kubeconfig: "~/.kube/config"

		// context is the Kubernetes context to use.
		// Override with --context flag or OPM_CONTEXT env var.
		// Default: current-context from kubeconfig
		context?: string

		// namespace is the default namespace for operations.
		// Override with --namespace flag or OPM_NAMESPACE env var.
		namespace: "default"
	}

	// log controls logging behavior.
	log: {
		// timestamps controls whether timestamps are shown in log output.
		// Override with --timestamps flag.
		timestamps: true

		// kubernetes controls Kubernetes-related log behavior.
		kubernetes: {
			// apiWarnings controls how K8s API deprecation warnings are displayed.
			// "warn"     - Show as WARN in log output
			// "debug"    - Only show with --verbose (default)
			// "suppress" - Drop entirely
			apiWarnings: "debug"
		}
	}
}
`, DefaultRegistry)

// DefaultPlatformTemplate is the embedded template for ~/.opm/platform.cue —
// the local default platform (enhancement 0006 D39, D21 precedence source 3).
// Data-only projection of the Platform CR spec: wrapped in
// apiVersion/kind/metadata this document IS the cluster singleton Platform.
//
// The seeded subscription ranges are explicit and prerelease-tolerant
// (Masterminds semantics: the -0 bounds admit prereleases, cap below v2);
// tighten to plain major-bounded ranges once the catalogs cut stable.
const DefaultPlatformTemplate = `// OPM local default platform
// Generated by: opm config init
// Used when no --platform flag is given and no cluster Platform CR is
// readable. Data only — CUE imports are not allowed in this file.
// Documentation: https://opmodel.dev/docs/cli/config

// name is the platform name. "cluster" matches the in-cluster singleton
// this file seeds on first apply against an empty cluster.
name: "cluster"

// type is an informational discriminator; it does not affect matching.
type: "kubernetes"

// registry subscribes this platform to catalogs by CUE module path.
// The kernel resolves each subscription against the OCI registry and
// selects the highest version satisfying filter.range.
registry: {
	"opmodel.dev/catalogs/opm": {
		filter: range: ">=1.0.0-0 <2.0.0-0"
	}
	"opmodel.dev/catalogs/kubernetes": {
		filter: range: ">=1.1.0-0 <2.0.0-0"
	}
}
`
